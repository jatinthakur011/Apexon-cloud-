# Arithmetic Operators
a = 50
b = 20
print(a + b)   # Addition  -->70
print(a - b)   # Subtraction -->30
print(a * b)   # Multiplication -->1000
print(a / b)   # Division -->2.5
print(a % b)   # Modulus -->10
print(a ** 2)  # Exponent -->2500
print(a // b)  # Floor Division -->2

# Operator Precedence Example

result1 = 10 + 5 * 2
print(result1)   # * runs before +

result2 = (10 + 5) * 2
print(result2)   # bracket runs first

result3 = 100 / 10 * 2
print(result3)   # left to right

result4 = 2 ** 3 ** 2
print(result4)   # right to left for because same precedence**

